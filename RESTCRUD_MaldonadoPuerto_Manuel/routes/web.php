Route::get('/', function () {
    return redirect()->route('monumentos.index');
});

Route::get('/dashboard', function () {
    return view('dashboard');
})->middleware(['auth', 'verified'])->name('dashboard');

Route::middleware('auth')->group(function () {
    Route::get('/profile', [ProfileController::class, 'edit'])->name('profile.edit');
    Route::patch('/profile', [ProfileController::class, 'update'])->name('profile.update');
    Route::delete('/profile', [ProfileController::class, 'destroy'])->name('profile.destroy');
    // Creacion del Token Documentacion
Route::post('/tokens/create', function (Request $request) {
    $token = $request->user()->createToken($request->token_name);
 
    return ['token' => $token->plainTextToken];
});
});

Route::resource('monumentos', MonumentoController::class)->middleware('auth');

Route::withoutMiddleware('auth')->get('monumentos', [MonumentoController::class, 'index'])->name('monumentos.index');

Route::get('monuments/crud', [MonumentoController::class,'crud'])->name('monuments.crud');

require __DIR__.'/auth.php';
